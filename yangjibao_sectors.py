def get_industry_standard_name(raw_name):
    """将东方财富行业板块名映射为养基宝标准名
    Returns: 标准名 or None (不在白名单中)
    """
    # 精确匹配
    if raw_name in INDUSTRY_NAME_MAP:
        std = INDUSTRY_NAME_MAP[raw_name]
        if std in INDUSTRY_SECTORS:
            return std
    # 如果原始名就在白名单中
    if raw_name in INDUSTRY_SECTORS:
        return raw_name
    # 模糊匹配：去掉II/III后缀
    clean = raw_name.replace('II', '').replace('Ⅲ', '').replace('Ⅱ', '').strip()
    if clean in INDUSTRY_NAME_MAP:
        std = INDUSTRY_NAME_MAP[clean]
        if std in INDUSTRY_SECTORS:
            return std
    if clean in INDUSTRY_SECTORS:
        return clean
    return None


